// Convert ISO-2 (e.g. "DE") to flag emoji ("🇩🇪") via regional indicator chars.
// Returns empty string for invalid input — never throws.
export function flagOf(code) {
  if (!code || typeof code !== 'string' || code.length !== 2) return ''
  const A = 0x1f1e6
  const upper = code.toUpperCase()
  return String.fromCodePoint(A + upper.charCodeAt(0) - 65)
       + String.fromCodePoint(A + upper.charCodeAt(1) - 65)
}

export function countryName(code) {
  return COUNTRIES.find(c => c.code === code)?.name || code || ''
}
